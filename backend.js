// ── PDF attachments: previews + inline rendering ─────────────────────────
// Convention: drop PDFs into briefings/data/items/<id>/attachments/. In the
// briefing source, write `<a data-pdf="attachments/foo.pdf">caption</a>`.
// At render time (local view AND publish) the resolver below ENHANCES the
// anchor — keeps the same DOM node but gives it an href + injects a
// <img> preview + caption + "view full document" CTA inside. Same posture
// as the deep-dive resolver: works with both static HTML and JSX-mounted
// briefings (MutationObserver picks up React-rendered nodes), and React's
// reconciler is undisturbed because we don't replace nodes — we decorate
// the original anchor in place.
//
// Preview images are JPGs rendered by macOS `sips` from the PDF's first
// page, cached at `<dir>/attachments/.previews/<basename>.jpg` and
// regenerated only when the PDF's mtime exceeds the preview's. The
// publish step calls ensurePdfPreview for every attachments/*.pdf before
// copying into the dist; the local view route generates them lazily on
// first request.

async function ensurePdfPreview(briefingDir, pdfRelPath) {
  // pdfRelPath is something like "attachments/foo.pdf" — we honor whatever
  // dir the author put the file in, with .previews as a sibling within
  // the same dir. Returns the relative path of the preview, or null if
  // anything failed (caller decides whether to fall back gracefully).
  if (!/\.pdf$/i.test(pdfRelPath)) return null;
  const pdfAbs = safeJoin(briefingDir, pdfRelPath);
  if (!pdfAbs) return null;
  let pdfStat;
  try { pdfStat = await fs.stat(pdfAbs); }
  catch { return null; }
  if (!pdfStat.isFile()) return null;

  const dir = path.dirname(pdfRelPath);
  const base = path.basename(pdfRelPath).replace(/\.pdf$/i, '.jpg');
  const previewRel = path.join(dir, '.previews', base);
  const previewAbs = safeJoin(briefingDir, previewRel);
  if (!previewAbs) return null;

  // Cache hit: preview newer than (or equal to) the PDF.
  try {
    const prevStat = await fs.stat(previewAbs);
    if (prevStat.mtimeMs >= pdfStat.mtimeMs) return previewRel;
  } catch {}

  await fs.mkdir(path.dirname(previewAbs), { recursive: true });
  return new Promise((resolve) => {
    const proc = spawn('/usr/bin/sips', [
      '-s', 'format', 'jpeg',
      '-s', 'formatOptions', '85',
      '-Z', '1200',
      pdfAbs, '--out', previewAbs,
    ], { stdio: 'ignore' });
    const timer = setTimeout(() => proc.kill('SIGKILL'), 30000);
    proc.on('close', (code) => {
      clearTimeout(timer);
      resolve(code === 0 ? previewRel : null);
    });
    proc.on('error', () => { clearTimeout(timer); resolve(null); });
  });
}

// Pre-generate previews for every attachments/*.pdf in a briefing dir.
// Errors don't abort — a missing preview just falls back to a broken
// <img> on the page, which is bad but recoverable, while a hard fail
// would block the whole publish.
async function ensureAllPdfPreviews(briefingDir) {
  const att = path.join(briefingDir, 'attachments');
  let entries;
  try { entries = await fs.readdir(att, { withFileTypes: true }); }
  catch { return; }
  for (const ent of entries) {
    if (!ent.isFile() || !/\.pdf$/i.test(ent.name)) continue;
    await ensurePdfPreview(briefingDir, path.join('attachments', ent.name));
  }
}

