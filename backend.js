// ── Sub-page support: breadcrumb + deep-dive resolver ────────────────────
// Both snippets are injected statically into served HTML. They use vanilla
// DOM (no React) so they sit alongside whatever the briefing's own runtime
// renders — same posture as the feedback widget. The deep-dive resolver
// runs once on load AND watches for new `[data-deepdive]` nodes via
// MutationObserver, so JSX-rendered buttons (like the Alberta seed's app.jsx)
// get their hrefs filled in once Babel mounts them.
//
// `base` is the URL prefix that resolves a child briefing's id to its root
// URL — `/api/briefings/view/item/` for atelier preview, `/` for the
// production Vercel deploy where every briefing sits at `/<id>/`.
function deepDiveResolverSnippet(base) {
  return `
<script>
(function () {
  var BASE = ${JSON.stringify(base)};
  function resolve(el) {
    var id = el.getAttribute('data-deepdive');
    if (!id) return;
    if (el.getAttribute('href')) return;
    el.setAttribute('href', BASE + id + '/');
  }
  function scan(root) {
    if (!root || !root.querySelectorAll) return;
    var els = root.querySelectorAll('[data-deepdive]');
    for (var i = 0; i < els.length; i++) resolve(els[i]);
  }
  scan(document);
  if (typeof MutationObserver !== 'undefined' && document.body) {
    new MutationObserver(function (records) {
      for (var i = 0; i < records.length; i++) {
        var added = records[i].addedNodes;
        for (var j = 0; j < added.length; j++) {
          var n = added[j];
          if (!n || n.nodeType !== 1) continue;
          if (n.matches && n.matches('[data-deepdive]')) resolve(n);
          scan(n);
        }
      }
    }).observe(document.body, { childList: true, subtree: true });
  }
})();
</script>
`;
}

