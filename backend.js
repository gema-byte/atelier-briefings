function runVercelDeploy(cwd) {
  return new Promise((resolve, reject) => {
    const proc = spawn('npx', ['-y', 'vercel@latest', '--prod', '--yes'], {
      cwd,
      stdio: ['ignore', 'pipe', 'pipe'],
      env: { ...process.env, FORCE_COLOR: '0', CI: '1' },
    });
    let stdout = '';
    let stderr = '';
    proc.stdout.on('data', (d) => { stdout += d; });
    proc.stderr.on('data', (d) => { stderr += d; });
    const timer = setTimeout(() => proc.kill('SIGKILL'), 180000);
    proc.on('close', (code) => {
      clearTimeout(timer);
      const out = stdout + '\n' + stderr;
      const alias      = (out.match(/Aliased:\s+(https:\/\/\S+)/)    || [])[1] || null;
      const deployment = (out.match(/Production:\s+(https:\/\/\S+)/) || [])[1] || null;
      if (code !== 0 && !alias && !deployment) {
        return reject(new Error(`vercel exited ${code}: ${(stderr || stdout).slice(-500).trim()}`));
      }
      resolve({ alias, deployment, stdout, stderr });
    });
    proc.on('error', reject);
  });
}

async function serveFile(absPath, res) {
  let stat;
  try { stat = await fs.stat(absPath); }
  catch { res.statusCode = 404; res.end('Not found'); return; }
  if (!stat.isFile()) { res.statusCode = 404; res.end('Not found'); return; }
  const buf = await fs.readFile(absPath);
  const type = MIME[path.extname(absPath).toLowerCase()] || 'application/octet-stream';
  res.writeHead(200, {
    'Content-Type': type,
    'Content-Length': buf.length,
    'Cache-Control': 'no-cache',
  });
  res.end(buf);
}

