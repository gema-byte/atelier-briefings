async function copyDir(src, dst) {
  await fs.mkdir(dst, { recursive: true });
  for (const ent of await fs.readdir(src, { withFileTypes: true })) {
    const s = path.join(src, ent.name);
    const d = path.join(dst, ent.name);
    if (ent.isDirectory()) await copyDir(s, d);
    else if (ent.isFile()) await fs.copyFile(s, d);
  }
}

function genId(name) {
  const slug = String(name || 'briefing')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 40) || 'briefing';
  return `${slug}-${randomBytes(3).toString('hex')}`;
}

function safeJoin(rootDir, relPath) {
  // Prevent traversal: resolved target must stay under root.
  const target = path.resolve(rootDir, relPath || '.');
  const rootAbs = path.resolve(rootDir);
  if (target !== rootAbs && !target.startsWith(rootAbs + path.sep)) return null;
  return target;
}

