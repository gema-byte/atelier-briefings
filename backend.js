async function readMeta(dir, fallbackId) {
  let raw;
  try { raw = await fs.readFile(path.join(dir, 'briefing.json'), 'utf8'); }
  catch { return null; }
  let meta;
  try { meta = JSON.parse(raw); } catch { return null; }
  return {
    name:        typeof meta.name        === 'string' ? meta.name        : fallbackId,
    description: typeof meta.description === 'string' ? meta.description : '',
    kind:        meta.kind === 'instance' ? 'instance' : 'template',
    entry:       typeof meta.entry       === 'string' ? meta.entry       : 'index.html',
    source:      typeof meta.source      === 'string' ? meta.source      : '',
    createdAt:   typeof meta.createdAt   === 'string' ? meta.createdAt   : null,
    publishUrl:  typeof meta.publishUrl  === 'string' ? meta.publishUrl  : null,
    publishedAt: typeof meta.publishedAt === 'string' ? meta.publishedAt : null,
    // Privacy state.
    lock:        meta.lock && typeof meta.lock === 'object' && typeof meta.lock.password === 'string'
                   ? { password: meta.lock.password } : null,
    expiresAt:   typeof meta.expiresAt   === 'string' ? meta.expiresAt   : null,
    unpublishedAt: typeof meta.unpublishedAt === 'string' ? meta.unpublishedAt : null,
    // Parent linkage. One level only — a child cannot itself be a parent.
    parentId:    typeof meta.parentId    === 'string' && ID_RE.test(meta.parentId)
                   ? meta.parentId : null,
  };
}

// Patch fields into a briefing's briefing.json without losing unknown keys.
async function patchMeta(briefingDir, fallbackId, patch) {
  let raw = '{}';
  try { raw = await fs.readFile(path.join(briefingDir, 'briefing.json'), 'utf8'); } catch {}
  let cur = {};
  try { cur = JSON.parse(raw); } catch {}
  const next = { ...cur, ...patch };
  await fs.writeFile(
    path.join(briefingDir, 'briefing.json'),
    JSON.stringify(next, null, 2) + '\n',
    'utf8',
  );
  return readMeta(briefingDir, fallbackId);
}

// Friendly password — three words + two digits, easy to type.
function generatePassword() {
  const A = ['blue','green','red','gold','warm','cool','quiet','quick','calm','sharp','bold','soft','copper','amber'];
  const B = ['stone','river','field','cloud','tree','peak','plain','star','bell','tide','bridge','wing','pine','ridge'];
  const pick = (xs) => xs[Math.floor(Math.random() * xs.length)];
  return pick(A) + '-' + pick(B) + '-' + Math.floor(10 + Math.random() * 90);
}

// Vercel Edge Middleware (auto-generated on each publish). Inlines the
// per-briefing lock + expiry settings so we don't need a runtime store
// on the deployed side. Lives at <publishRoot>/middleware.ts; Vercel
// picks it up automatically and runs it on every matching request.
function renderMiddlewareSource(config) {
  return `// Auto-generated by atelier briefings on each publish — do not edit by hand.
// Lives at the dist root so Vercel runs it as an Edge Function on every
// request. Per-briefing lock + expiry settings are inlined below; if a
// briefing has neither, it never reaches the gates here.

const CONFIG = ${JSON.stringify(config, null, 2)};

export const config = {
  // Skip Vercel's own asset paths so this never ships their requests
  // through the gate logic.
  matcher: '/((?!_next|_vercel|favicon).*)',
};

export default function middleware(req) {
  const url = new URL(req.url);
  const m = url.pathname.match(/^\\/([a-z0-9][a-z0-9_-]*)\\//);
  if (!m) return;
  const id = m[1];
  const c = CONFIG[id];
  if (!c) return;

  if (c.expiresAt && Date.now() > Date.parse(c.expiresAt)) {
    return new Response(
      '<!doctype html><html><head><meta charset="utf-8"><title>Expired</title></head>' +
      '<body style="font-family:ui-sans-serif,system-ui;text-align:center;padding:80px 24px;background:#fbf9f4;color:#2a3328">' +
      '<h1 style="font-weight:500;font-size:18px;margin:0 0 8px">This briefing has expired.</h1>' +
      '<p style="color:#6e6a5e;font-size:13px;margin:0">If you still need access, ask the sender to extend it.</p>' +
      '</body></html>',
      { status: 410, headers: { 'content-type': 'text/html; charset=utf-8' } }
    );
  }

  if (c.password) {
    const auth = req.headers.get('authorization') || '';
    const expected = 'Basic ' + btoa('briefing:' + c.password);
    if (auth !== expected) {
      return new Response('Authentication required', {
        status: 401,
        headers: { 'WWW-Authenticate': 'Basic realm="Briefing", charset="UTF-8"' },
      });
    }
  }
}
`;
}

async function regenerateMiddleware(publishRoot, ctx) {
  const itemsDir = path.join(ctx.dataDir, 'items');
  const config = {};
  for (const root of [SEED_DIR, itemsDir]) {
    let names = [];
    try { names = await fs.readdir(root); } catch { continue; }
    for (const name of names) {
      if (!ID_RE.test(name)) continue;
      const dir = path.join(root, name);
      let stat;
      try { stat = await fs.stat(dir); } catch { continue; }
      if (!stat.isDirectory()) continue;
      const meta = await readMeta(dir, name);
      if (!meta) continue;
      // Only gate briefings actually present in the dist (i.e., live).
      if (!existsSync(path.join(publishRoot, name))) continue;
      const c = {};
      if (meta.lock?.password) c.password = meta.lock.password;
      if (meta.expiresAt)      c.expiresAt = meta.expiresAt;
      if (Object.keys(c).length) config[name] = c;
    }
  }
  const mwPath = path.join(publishRoot, 'middleware.ts');
  if (!Object.keys(config).length) {
    await fs.rm(mwPath, { force: true });
    return;
  }
  await fs.writeFile(mwPath, renderMiddlewareSource(config), 'utf8');
}

